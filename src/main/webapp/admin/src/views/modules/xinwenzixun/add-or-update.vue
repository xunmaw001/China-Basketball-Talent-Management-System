						if(o=='fengmianbeijing'){
							this.ruleForm.fengmianbeijing = obj[o];
							this.ro.fengmianbeijing = true;
							continue;
						}
						if(o=='bianjiren'){
							this.ruleForm.bianjiren = obj[o];
							this.ro.bianjiren = true;
